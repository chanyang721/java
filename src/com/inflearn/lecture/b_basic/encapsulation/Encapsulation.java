package com.inflearn.lecture.b_basic.encapsulation;

public class Encapsulation {
    public static void main(String[] args) {
        /*
        * 캡슐화
        *   - 필드와 메서드를 하나의 클래스로 모으는 것
        *   - 클래스에 정의된 필드와 메서드에 대한 외부 접근 제한하는 것
        *       - 모든 필드의 직접적인 변경 방지 -> 모든 필드는 private이 기본이다.
        *       - 필드는 객체의 메서드에서 필드 변경에 제한된 로직과 같이 접근되어야 한다.
        *       - 노출이 꼭 필요한 메서드만 노출한다. -> 자동차 핸들, 엑셀, 브레이크 등은 외부에 노출
        *
        * - 객체 사용자의 복잡성을 낮춘다.
        * */
    }
}
